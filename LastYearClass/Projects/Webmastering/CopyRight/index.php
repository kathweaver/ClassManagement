          <FONT SIZE="+2" FACE="verdana,arial,helvetica"><B>Introduction</B></FONT></P>
        <P><font face="verdana,arial,helvetica">You work for a company that has 
          written a web site, including graphics and music. The music was music 
          composed for your client and is already being used in their advertising. 
          Your job is to find out how to copyright your website, how to copyright 
          the graphics, and how to indicate that the music is under copyright 
          and cannot be used by others.</font></P>
