        <FONT SIZE="+2" FACE="verdana,arial,helvetica"><B>The Process</B></FONT> 
        <p></P>
        <P><FONT FACE="verdana,arial,helvetica">Follow the following steps to 
          come to your decision.</FONT></P>
        <OL>
          <LI><font face="verdana,arial,helvetica">Research copyrights and how 
            copyrights relate to software. Make a journal documenting the research 
            you have done in a word document called Copyright.doc -- This is only 
            a journal or documentation of the resource you've done. Record the 
            links you've used and summarize what they had to say. Spelling, grammar, 
            etc. do not count. The process and documenting does! Remember programmers 
            write down what they do.</font></LI>
          <LI>Decide how to copyright your content.</LI>
          <LI>Decide how to protect your images. Is copyrighting enough? <b></b>See<b><font face="verdana,arial,helvetica"> 
