      <li>At least one graphic downloaded from the internet -- make sure you show 
        proper credit for that graphic.</li>
    </ul>
  </li>
  <li>Links 
    <ul>
      <li>At least one &quot;mailto&quot; link to your Gaggle net email account</li>
