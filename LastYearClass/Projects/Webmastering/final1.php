<H2 align="center">Final Webmastering Project</H2>
<p>You will be working on this assignment while we review for the ACP. This project 
  will also count as the final exam for any seniors needing a final exam grade.</p>
<p>You are to create a website using the information you have been given during 
  the year. You may use any tools you wish to create the website and the graphics. 
  You may do your website on any one of the following topics:</p>
<ul>
  <li>Personal website, describing your hobbies and interests</li>
  <li>Website on an organization you belong to, this could be a sports team, club, 
    or any other type of organization.</li>
  <li>Website on a research project. If you have a research project for another 
    class, you can use that information to create the website. </li>
</ul>
<p>You must decide on a topic by April 15 and you MUST send me an email with the 
  topic to kw3922@dallasisd.org 
<p>Your website must contain the following:
<ul>
