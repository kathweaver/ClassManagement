        <P><A NAME=Introduction></A><BR>
          <FONT SIZE="+2" FACE="verdana,arial,helvetica"><B>Introduction</B></FONT></P>
        <P><font face="verdana,arial,helvetica">You work for a company that has 
          developed two pieces of software as the result of writing a game program. 
          One piece of software is the graphics engine, the second piece of software 
          is the actual game. You have been asked to research copyright and licenses. 
          After researching this, you have to decide how to license your software, 
          and if you want to license each piece of software the same. </font></P>
        <P>&nbsp;</P>
        <P> 
        <FONT SIZE="+1"> 
        <HR ALIGN=left>
