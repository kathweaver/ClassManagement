            that explains why you have made the decision you have made</font><FONT FACE="verdana,arial,helvetica">.</FONT><FONT SIZE="+1"> 
            </FONT></LI>
        </UL>
        <p><font size="+1">These links are just to get you started. EXPLORE, do 
          web searches and think about what is going on. Read some technology 
          magazines that deal with software licensing.</font></p>
        <p>You are successful if you have been thinking seriously about the issues 
          involved. Also remember, there is NO right answer here. Different companies 
          have made different decisions for different reasons.<FONT SIZE="+1"> 
          </FONT></p>
        <p>&nbsp;</p>
        <FONT SIZE="+1"><HR ALIGN=left>
        </FONT><A NAME=Process></A><BR>
        <FONT SIZE="+2" FACE="verdana,arial,helvetica"><B>The Process</B></FONT> 
        <p></P>
        <P><FONT FACE="verdana,arial,helvetica">Follow the following steps to 
          come to your decision.</FONT></P>
        <OL>
          <LI><font face="verdana,arial,helvetica">Research copyrights and how 
            copyrights relate to software. Make a journal documenting the research 
            you have done in a word document called Copyright.doc -- This is only 
            a journal or documentation of the resource you've done. Record the 
            links you've used and summarize what they had to say. Spelling, grammar, 
            etc. do not count. The process and documenting does! Remember programmers 
            write down what they do.</font></LI>
          <LI><font face="verdana,arial,helvetica">Research various licenses and 
            determine why a company would use that type of document. Make a journal 
            documenting the research you have done in a word document called License.doc 
            -- Again, we are looking for documentation on the process.</font></LI>
          <LI><font face="verdana,arial,helvetica">Make a decision on how to license 
            your graphics engine so that other companys can use it. Summarize 
            your reasoning and put it in a word document called decision.doc.</font></LI>
          <LI><font face="verdana,arial,helvetica">Make a decision on how to license 
            your game program. Summarize your reasoning and add it to your decision.doc 
            file. </font></LI>
          <LI><font face="verdana,arial,helvetica">Pull the above information 
            together and put it in a PowerPoint presentation called Project2.ppt 
            that can be published on the internet.</font></LI>
        </OL>
