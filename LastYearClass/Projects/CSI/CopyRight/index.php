        <FONT SIZE="+2" FACE="verdana,arial,helvetica"><B>The Task</B></FONT> 
        <p></P>
        <P><font face="verdana,arial,helvetica">At the end of the project you 
          will have accomplished the following:</font></P>
        <UL>
          <LI><font face="verdana,arial,helvetica">know how copyrights are applied 
            to and effect programming (and websites) -- here are some websites 
            to get your started. Do a web search using the term: copyright + software 
            or copyright + programs to find your own links.</font> 
            <ul>
              <li><a href="http://www.benedict.com/">Copyright Web page -- information 
                on copyrighting web pages, and digital items (software)</a></li>
              <li><a href="http://www.htmlgoodies.com/tutors/copyright.html">HTML 
                Goodies on Copyright</a></li>
              <li><font face="verdana,arial,helvetica"><a href="http://www.templetons.com/brad/copymyths.html">10 
                Myths about Copyrights Explained</a> </font></li>
